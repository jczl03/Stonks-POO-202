    public void fillProductComboBox(ResultSet rs){
        try {
            while(rs.next()){
                ProductosComboBox.addItem(rs.getString("nombre"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(GraficosPestana.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
